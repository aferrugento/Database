    private void jButton47ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton47ActionPerformed
        // TODO add your handling code here:
        String dest = String.valueOf(jTextField30.getText());
        String subject = String.valueOf(jTextField32.getText());
        String mess = String.valueOf(jTextField33.getText());
        String day = String.valueOf(jTextField36.getText());
        String mouth = String.valueOf(jTextField34.getText());
        String year = String.valueOf(jTextField35.getText());
        String hour = String.valueOf(jTextField37.getText());
        String min = String.valueOf(jTextField38.getText());
        String path = "";
        if (jCheckBox5.isSelected()) {
            path = jTextField31.getText();
        }

        if (!day.equals("") && !mouth.equals("") && !year.equals("") && !hour.equals("") && !min.equals("")) {
            try {
                aux.delayedMessages(Integer.parseInt(day), Integer.parseInt(mouth), Integer.parseInt(year), Integer.parseInt(hour), Integer.parseInt(min),
                        dest, subject, mess, path);
                NewMessage.setVisible(false);
                Messages.setBounds(430, 200, 400, 350);
                Messages.setVisible(true);
            } catch (RemoteException ex) {
                Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            int messid = aux.message(subject, mess, dest);
            if (!path.trim().equals("")) {
                aux.addAttachment(path, messid);
            }
            if (messid != -1) {
                JOptionPane.showMessageDialog(jLabel1, "Message Sent!", "Information", JOptionPane.INFORMATION_MESSAGE);
                jTextField30.setText("");
                jTextField32.setText("");
                jTextField33.setText("");
                jTextField34.setText("");
                jTextField35.setText("");
                jTextField36.setText("");
                jTextField37.setText("");
                jTextField38.setText("");
                NewMessage.setVisible(false);
                Messages.setBounds(430, 200, 400, 350);
                Messages.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(jLabel1, "Message not Sent", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton47ActionPerformed

