        // TODO add your handling code here:
        String theme;
        for (int i = 0; i < jTable10.size().getHeight(); i++) {
            if (jTable10.isRowSelected(i)) {
                theme = (String) jTable10.getValueAt(i, 0);
                for (int j = 0; j < chat.size(); j++) {
                    if (chat.get(j).getTheme().equals(theme)) {
                        temp = chat.get(j);
                        int[] perm = aux.permissions(temp);
                        if (perm[0] == 0 && perm[1] == 0) {
                            JOptionPane.showMessageDialog(jLabel1, "You dont have any permissions in this chatroom!", "Information", JOptionPane.INFORMATION_MESSAGE);
                            return;
                        } else if (perm[0] == 0 && perm[1] == 1) {
                            jButton59.setEnabled(false);
                        }
                        publicMess = aux.listMessagesChatrooms(temp);
                        attachs = aux.listAttachs();
                        String string = "";
                        Boolean flag = false;
                        for (int k = 0; k < publicMess.size(); k++) {
                            flag = false;
                            for (int n = 0; n < attachs.size(); n++) {
                                if (publicMess.get(k).getMessageID() == attachs.get(n).getMess()) {

                                    string += ("FROM: " + publicMess.get(k).getSource().getUsername() + "\nCONTENT: " + publicMess.get(k).getContent()
                                            + "\nSEND TIME: " + publicMess.get(k).getSendTime().getDate() + "/" + (publicMess.get(k).getSendTime().getMonth() + 1) + "/" + (publicMess.get(k).getSendTime().getYear() + 1900)
                                            + "  " + publicMess.get(k).getSendTime().getHours() + "h:" + publicMess.get(k).getSendTime().getMinutes()
                                            + "\nATTACHMENTS: " + attachs.get(n).getPath() + "\n\n");
                                    flag = true;
                                }
                            }
                            if (!flag) {
                                string += ("FROM: " + publicMess.get(k).getSource().getUsername() + "\nCONTENT: " + publicMess.get(k).getContent()
                                        + "\nSEND TIME: " + publicMess.get(k).getSendTime().getDate() + "/" + (publicMess.get(k).getSendTime().getMonth() + 1) + "/" + (publicMess.get(k).getSendTime().getYear() + 1900)
                                        + "  " + publicMess.get(k).getSendTime().getHours() + "h:" + publicMess.get(k).getSendTime().getMinutes()
                                        + "\nATTACHMENTS: \n\n");
                            }
                        }

                        jTextArea1.setText(string);

                        ChatroomsJoined.setVisible(false);
                        Chat.setBounds(430, 200, 450, 450);
                        Chat.setVisible(true);

                    }
                }
            }
        }
