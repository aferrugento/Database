    private void jButton38ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton38ActionPerformed
        // TODO add your handling code here:
        Object[] obj = new Object[7];
        String a = String.valueOf(jTextField27.getText());
        obj[0] = a;
        String nationality = String.valueOf(jTextField24.getText());
        obj[1] = nationality;
        String email = String.valueOf(jTextField23.getText());
        obj[2] = email;
        String job = String.valueOf(jTextField29.getText());
        obj[3] = job;
        String city = String.valueOf(jTextField39.getText());
        obj[4] = city;
        String interests = String.valueOf(jTextField26.getText());
        obj[6] = interests;

        String s = String.valueOf(jTextField28.getText());
        if (!s.equals("")) {
            if (s.toLowerCase().equals("female")) {
                s = "f";
            } else if (s.toLowerCase().equals("male")) {
                s = "m";
            } else {
                JOptionPane.showMessageDialog(jLabel1, "Gender invalid! Please try again.", "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
        }
        obj[5] = s;

        if (a.equals("") && nationality.equals("") && email.equals("") && job.equals("") && city.equals("") && interests.equals("") && s.equals("")) {
            JOptionPane.showMessageDialog(jLabel1, "Fill in the fiels you want!", "Information", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else {
            users = aux.searchUserBy(obj);
            DefaultTableModel results = new DefaultTableModel() {
                public boolean isCellEditable(int row, int col) {
                    return false;
                }
            };
            results.addColumn("Username:");

            jTable5.setModel(results);
            jTable5.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

            jTable5.getTableHeader().setFont(new Font("Bell MT", Font.PLAIN, 16));

            if (results.getRowCount() > 0) {
                while (results.getRowCount() > 0) {
                    results.removeRow(0);
                }
            }

            int i;
            if (users != null) {
                for (i = 0; i < users.size(); i++) {
                    if (users.get(i).isActive()) {
                        results.addRow(new Object[]{
                                    users.get(i).getUsername()});
                    }
                }
            }
            SearchUser.setVisible(false);
            ResultsUsers.setBounds(430, 200, 400, 350);
            ResultsUsers.setVisible(true);
        }
        jTextField27.setText("");
        jTextField24.setText("");
        jTextField23.setText("");
        jTextField29.setText("");
        jTextField26.setText("");
        jTextField28.setText("");
    }//GEN-LAST:event_jButton38ActionPerformed

